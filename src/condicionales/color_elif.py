# En este caso, 
# si el valor de color no coincide con ninguna de las condiciones, 
# simplemente no se ejecuta ningún bloque de código.

color = "azul"

if color == "rojo":
    print("El color es rojo.")
elif color == "verde":
    print("El color es verde.")
elif color == "azul":
    print("El color es azul.")