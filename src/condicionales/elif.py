# El programa verifica si el número es positivo (mayor a cero)
#  o negativo (menor a cero).
#  Si no cumple ninguna de estas condiciones, concluye que el número es cero. 

numero = 0

if numero > 0:
    print("El número es positivo.")
elif numero < 0:
    print("El número es negativo.")
else:
    print("El número es cero.")