# Comparando con el uso tradicional de if-else, el beneficio principal de las expresiones 
# condicionales radica en su capacidad para escribir código más compacto sin crear bloques adicionales. Por ejemplo:

# Uso de if-else tradicional
if condición:
    variable = valor_verdadero
else:
    variable = valor_falso

# Uso de expresión condicional
variable = valor_verdadero if condición else valor_falso